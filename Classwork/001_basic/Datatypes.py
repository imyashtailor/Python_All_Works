# numeric : int,float,complex

#1. integer
a = 10
print(type(a))

#2. float
b = 10.5
print(type(b))

#3. complex
c = 4j+5
print(type(c))

#sequence : str,list,tuple,range
name = "Yash"
print(type(name))

#list : list datatype
l = [10,20,30,40]
print(type(l))

#tuple : tuple datatype
t = (10,20,30,40)
print(type(t))

#set : set
s = {10,20,30,40}
print(type(s))

#map : dict
d = {"nam" : "Yash", "email" : "y@gmail.com"}
print(type(d))

#boolean : bool
is_true = True
print(type(is_true))

#type conversion :- one type convert to another datatype
a = "xyz"
print(type(a))
k = int(a)
print(type(k))



